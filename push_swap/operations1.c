#include "push_swap.h"

void	swap(t_list **stack)
{
	int		tmp;
	t_list	*list;

	if (!stack || !*stack || !(*stack)->next)
		return ;
	list = *stack;
	tmp = list->content;
	list->content = list->next->content;
	list->next->content = tmp;
}

void	sa(t_list **stack_a)
{
	swap(stack_a);
	ft_printf("sa\n");
}

void	sb(t_list **stack_b)
{
	swap(stack_b);
	ft_printf("sb\n");
}

void	ss(t_list **stack_a, t_list **stack_b)
{
	swap(stack_a);
	swap(stack_b);
	ft_printf("ss\n");
}
